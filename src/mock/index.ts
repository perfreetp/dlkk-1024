    d.setDate(d.getDate() - i);
    const dateStr = `${d.getMonth() + 1}/${d.getDate()}`;
    const baseActive = 180 + Math.floor(Math.random() * 60);
    const baseLogin = baseActive * (3 + Math.random() * 2);
